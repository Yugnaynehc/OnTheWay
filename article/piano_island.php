        <p class="show">鼓浪屿：钢琴小岛</p>     
            
        <div id="article">
        	<h3></h3>
                     
            <p>有人说过，时间是用来浪费的。</p>
            <p>有人说过，总有这么一个地方，可以让你"忙中偷闲"，闲庭信步；观潮起潮落，听涛声阵阵。</p>
            <p>启程去鼓浪屿，一个缓慢的不能再缓慢的小岛。</p>
            <p>做一个幸福的人，就在那时，那天。</p>
            
        	<img class="picture" src="../images/theme/GLY_1/GLY1_06.JPG"/>
            <p>透过阳台的花盆可以看见海，对面就是环岛公路，你会发现很多人或步行或骑车在慢悠悠地享受阳光海滩，偶尔顿首，海浪四起。japaul于行云旅社</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_19.JPG"/>
            <p>曾厝垵的村民喜欢把自己的家布置得如同花园，老板会很亲切地告诉你去鼓浪屿坐29路在轮渡站下车。球场大排档还不错，旁边有家烧烤店，楼下傍晚的时候总会有很多旅者弹弹吉他唱唱歌。japaul于行云旅社</p>
            	
            <img class="picture" src="../images/theme/GLY_1/GLY1_00.JPG"/>
        	<p>很多路上少有人行，予散布的人以奢侈的自由。小巷曲折地延伸，走下去，或者就到了另一条路上，那就继续走下去；或者就是尽头，也没关系。原来看过左边的风景，现在转身看着右边的风景。japaul于鼓浪屿</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_01.JPG"/>
            <p>走进去一家咖啡旅馆，主人在忙着泡咖啡，看见你会对你会心的一笑，你随意地环视，幽静的小院，下午的阳光伴随风儿吹过，风铃摇曳，铃声响起，让你的心情便顿时安静了许多。japaul于鼓浪屿</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_02.JPG"/>
            <p>你或许会记下这条路的名字，可能是鼓新路，是安海路，是泉州路，是笔山路，是内厝沃路，或者是你私心喜欢的任何一条路。离开这个小岛之后，也许哪一天，它又会在你的记忆里浮现，那些慢慢的，悠长的，安静的光影流年。japaul鼓浪屿</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_03.JPG"/>
            <p>街角的咖啡店，飘着恍惚的音乐，慢慢地进去，随意地坐着，一个发呆的人。那些旅者走得累了，也就走进去，坐下来，一起发呆，整理下行囊，也整理下一路的收获。japaul于邂逅咖啡店</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_17.JPG"/>
            <p>要杯烧仙草，看会儿书，人很少，你可以坐到很晚，咖啡店里的这些细碎的东西，很容易就被步履匆匆的人忽略。japaul于邂逅咖啡店</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_04.JPG"/>
            <p>写个明信片，盖上娜雅或者张三疯的章子，寄给一些熟悉的或者不常联系的朋友，久未提笔写信的我突然间想起了曾经……japaul于画廊旅馆</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_05.JPG"/>
            <p>或者你是一个不太喜欢逛店的人，但是不得不为这个称为"猫岛"的小岛流连忘返，每家店各有洞天。</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_07.JPG"/>
            <p>这个岛可以让人忘记流年，似乎去年如此，今年也如此，浮云一别，流水几年，时间的脚步在这里都走得比别处缓慢。japaul于鼓浪屿</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_08.JPG"/>
            <p>在小岛上你最好迷路，不知道去了哪儿，你也许遇见几只猫在悠闲的散步，或许不小心闯进了别人家的院子，或许在静谧的墙角看见几朵开着正好的未名小花，或许转过一条小巷刚好听见涛声……japaul于鼓浪屿</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_09.JPG"/>
            <p>你永远不知道下一个路口有着什么样的风景，会遇到哪一个颜色的小花，哪个轻盈飘过的姑娘，哪个店的主人在亲切地抚摸自己家的猫咪，平淡的生活总是让人感觉时间很慢很慢……japaul于厦门大学湖畔咖啡店</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_10.JPG"/>
            <p>没有了烦恼，宠辱皆忘，没有了明天去做什么，该做什么，听听音乐，可以闻见主人养的不知名的小花香，眯一会，就一会儿……japau于画廊旅馆</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_11.JPG"/>
            <p>厦大对面是家有着"家庭书房"之称的光合作用书店，橘黄色的灯光映照出温馨的归家感，里面安静地飘着悠扬的曲子，累了，就去里面坐坐，看会书，小眯一会都是可以的。japaul于光合作用书店</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_15.JPG"/>
            <p>你想买本书，《最厦门》，详细地介绍了厦门好玩好吃好逛的地方。《迷失鼓浪屿》，拿着这本书去鼓浪屿的"花时间咖啡馆"去找air夫妇签名，或许他们不在，或许咖啡馆已经打烊，就像他们说过的那句话"遇见就遇见，错过就错过！"japaul于光合作用书店</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_13.JPG"/>
            <p>可能你听到哪一家飘出的细碎琴声，弹奏的是不是你喜欢的那支曲子，也不知会不会遇到路人甲乙丙，然后从此成了朋友。还是慢下来，慢慢慢慢地走。鼓浪屿是不喜欢行色匆匆的旅人的。japaul于厦大白城</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_14.JPG"/>
            <p>住家的墙头总会有三角梅一丛一丛，偶尔是朱槿，大朵，层叠的花瓣很圆满，或者就是不知名的小花，自由地开，自由地落。偶尔人家的院子里还种着木瓜，幽幽生长。很多树木花草，我尚不知晓它们的名字，几时有机会，拿一本植物图鉴，慢慢寻找对照过去，想一想就神往不已。japaul于鼓浪屿</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_12.JPG"/>
            <p>喝杯茶，突然想起了方才走过的沙滩，是白色柔软的，伴随袅袅升起的茶气，仿佛一切在梦里……japaul于画廊旅馆。</p>
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_16.JPG"/>
            <p>小孩子在自己门口玩，偶尔三五个人笑着闹着闪进一条巷子里，老人们用闽南话讲话，听得懂的会发现无非日常琐事，听不懂的也好以此当作眼前日常画面的配音。居民提着菜场里买来的菜，消失在巷子的尽头。有人摆了桌子在街边打牌，喝功夫茶，人生就这么慢下来，闲适无边。japaul于鼓浪屿</p>
                     
            
            <img class="picture" src="../images/theme/GLY_1/GLY1_18.JPG"/>
            <p>老宅的门楼、建材、屋顶、门窗、脚线，除去那些具体的描述，凝固在其中的时光，只有驻足观赏的时候，才能体会。你看到那些树木投下的影子，看到悄悄生长的青苔，看到一只鸟从屋顶飞过。japaul于鼓浪屿</p>           
            
            
